// Switch example
const day = "Thursday"

switch (day) {
    // Case 1
    case "Monday":
        console.log("Day is monday");
        break;
        
    // Case 2
    case "Tuesday":
        console.log("Day is tuesday");
        break;
        
    // Case 3
    case "Wednesday":
        console.log("Day is wednesday");
        break;
        
    // Case 4
    case "Thursday":
        console.log("Day is thursday");
        break;
    
    // Case 5
    case "Friday":
        console.log("Day is friday");
        break;

    // Case 6
    case "Saturday":
        console.log("Day is saturday");
        break;
    
    // Case 7    
    case "Sunday":
        console.log("Day is Sunday");
        break;
    
    default:
        console.log("Please entr the correct day");
        break;
}
